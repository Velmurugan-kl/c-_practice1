namespace InfoNameSpace
{
    public class Info
    {
        public string name;
        public string email;
        public uint phone;
        public string notes;

        public Info(string name, string email, uint phone, string notes)
        {
            this.name = name;
            this.email = email;
            this.phone = phone;
            this.notes = notes;
        }
    }
}
